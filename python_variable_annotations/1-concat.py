#!/usr/bin/env python3
''' Description: defines a Python function called concat that
takes two string arguments, str1 and str2, and returns a new
string that is the concatenation of str1 and str2.
    arguments: str1: str
               str2: str
'''


def concat(str1: str, str2: str) -> str:
    ''' Return concatenation of str1 and str2. '''
    return str1 + str2
